package com.example.rpcum.studentdirectory;

public class StudentPersonal {

    //fields
    private String username;
    private String read;
    private String movies;
    private String hookup;
    private String sports;
    private String hiking;
    private String religious;
    private String socialMedia;
    private String drink;
    private String smoke;
    private String music;

    //constructors
    public StudentPersonal() {}

    public String getUsername() {
        return username;
    }

    public String getRead() {
        return read;
    }

    public void setRead(String read) {
        this.read = read;
    }

    public String getMovies() {
        return movies;
    }

    public void setMovies(String movies) {
        this.movies = movies;
    }

    public String getHookup() {
        return hookup;
    }

    public void setHookup(String hookup) {
        this.hookup = hookup;
    }

    public String getSports() {
        return sports;
    }

    public void setSports(String sports) {
        this.sports = sports;
    }

    public String getHiking() {
        return hiking;
    }

    public void setHiking(String hiking) {
        this.hiking = hiking;
    }

    public String getReligious() {
        return religious;
    }

    public void setReligious(String religious) {
        this.religious = religious;
    }

    public String getSocialMedia() {
        return socialMedia;
    }

    public void setSocialMedia(String socialMedia) {
        this.socialMedia = socialMedia;
    }

    public String getDrink() {
        return drink;
    }

    public void setDrink(String drink) {
        this.drink = drink;
    }

    public String getSmoke() {
        return smoke;
    }

    public void setSmoke(String smoke) {
        this.smoke = smoke;
    }

    public String getMusic() {
        return music;
    }

    public void setMusic(String music) {
        this.music = music;
    }
}
